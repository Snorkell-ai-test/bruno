export const parseQueryParams = (query) => {
  if (!query || !query.length) {
    return [];
  }

  let params = query.split('&').map((param) => {
    let [name, value = ''] = param.split('=');
    return { name, value };
  });

  return filter(params, (p) => hasLength(p.name));
};

